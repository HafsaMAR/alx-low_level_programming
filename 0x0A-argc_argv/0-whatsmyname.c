#include <stdlib.h>
#include <stdio.h>
/**
 * main - Entry point
 * Description: program that prints its name, followed by a new line.
 * @argc: argument count
 * @argv: argument vector
 * Return: Always 0 (success)
 */
int main(int __attribute__ ((unused)) argc, char *argv[])
{
	printf("%s\n", argv[0]);
	return (0);
}
