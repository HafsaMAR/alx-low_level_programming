#include <stdlib.h>
#include <stdio.h>

/**
 * main - Entry point
 * Description: program that prints the number of arguments passed into it
 * @argc: argument counter
 * @argv: argument vector
 * Return: Always 0 (success)
 */
int main(int argc, char __attribute__((unused)) *argv[])
{
	printf("%d\n", argc);
	return (0);
}
